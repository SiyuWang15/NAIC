        x = self.layer4(x)
        x = self.layer3(x)
        x = self.layer2(x)
        x = self.layer1(x)
        x = self.conv1(x)
        # x = torch.sigmoid(self.conv1(x))
        # x = x.view(x.size(0), 2, 16, 32)
        return x

